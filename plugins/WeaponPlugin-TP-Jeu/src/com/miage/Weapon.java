package com.miage;

/**
 *
 * @author romain
 */
public class Weapon {

    private String id;
    private String label;

    public Weapon() {
    }

    public Weapon(String id, String label) {
        this.id = id;
        this.label = label;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
    
}
